
//set color one
document.getElementById('pickOne') .onclick = function(){
    document.querySelector('.hide-one') .style = 'display: flex';

    //pick blue
    document.getElementById('pickBlueOne') .onclick = function(){
        document.getElementById('pickOne') .style = 'background-color: blue';
    }

    //pick red
    document.getElementById('pickRedOne') .onclick = function(){
        document.getElementById('pickOne') .style = 'background-color: red';
    }

    //pick orange
    document.getElementById('pickOrangeOne') .onclick = function(){
        document.getElementById('pickOne') .style = 'background-color: orange';
    }

    //pick yello
    document.getElementById('pickYelloOne') .onclick = function(){
        document.getElementById('pickOne') .style = 'background-color: yellow';
    }

    //close
    document.getElementById('setPickOne') .onclick = function(){
        document.querySelector('.hide-one') .style = 'display: none'
        console.log('hi');
    }
}

